def llm_request(model, system_prompt, user_prompt):
    print(f"[llm_request] calling model={model}")
    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt}
        ]
    )
    content = response.choices[0].message.content
    print(f"[llm_request] received model={model} len={len(content)}")
    return content

