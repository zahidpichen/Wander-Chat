embedding_model = SentenceTransformer("sentence-transformers/all-MiniLM-L6-v2")

CHUNK_SIZE = 500
CHUNK_OVERLAP = 50


# ── Text Extraction ───────────────────────────────────────────────────────────

def extract_pdf(file_path):
    text = ""
    with pdfplumber.open(file_path) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + "\n"
    return text


def extract_docx(file_path):
    doc = Document(file_path)
    return "\n".join([para.text for para in doc.paragraphs if para.text.strip()])


def extract_text(file_path):
    if file_path.endswith(".pdf"):
        return extract_pdf(file_path)
    elif file_path.endswith(".docx") or file_path.endswith(".doc"):
        return extract_docx(file_path)


# ── Chunking ──────────────────────────────────────────────────────────────────

def chunk_text(text):
    words = text.split()
    chunks = []
    start = 0
    while start < len(words):
        end = start + CHUNK_SIZE
        chunk = " ".join(words[start:end])
        chunks.append(chunk)
        start += CHUNK_SIZE - CHUNK_OVERLAP
    return chunks


# ── FAISS Index ───────────────────────────────────────────────────────────────

faiss_index = None
chunk_store = []


def build_index(file_paths):
    global faiss_index, chunk_store

    all_chunks = []
    for fp in file_paths:
        text = extract_text(fp)
        chunks = chunk_text(text)
        all_chunks.extend(chunks)

    chunk_store = all_chunks
    embeddings = embedding_model.encode(all_chunks, show_progress_bar=True)
    embeddings = np.array(embeddings).astype("float32")

    dim = embeddings.shape[1]
    faiss_index = faiss.IndexFlatL2(dim)
    faiss_index.add(embeddings)

    print(f"Index built with {len(all_chunks)} chunks from {len(file_paths)} files.")


def retrieve(query, top_k=5):
    query_embedding = embedding_model.encode([query])
    query_embedding = np.array(query_embedding).astype("float32")

    distances, indices = faiss_index.search(query_embedding, top_k)
    results = [chunk_store[i] for i in indices[0] if i < len(chunk_store)]
    return "\n\n".join(results)

