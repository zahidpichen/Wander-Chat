    parsed = json.loads(raw)
    queries = parsed["search_queries"]
    missing_info = parsed.get("missing_or_outdated", None)
    web_data = []
    for i, q in enumerate(queries):
        print(f"[build_and_search] searching query {i+1}/{len(queries)}: {q}")
